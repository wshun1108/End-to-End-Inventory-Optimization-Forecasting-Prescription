# Calculate derived parameters
daily_holding_rate = annual_holding_rate / 365

# --- 4. Core Logic: Single Product Real-time Backtesting Engine ---
# Filter data for the currently selected product
df = df_all[df_all['item_id'] == selected_item].sort_values('date').reset_index(drop=True).copy()

# Dynamically calculate costs (based on user slider input for margin)
df['cost'] = df['sell_price'] * (1 - margin)
df['daily_holding_unit_cost'] = df['cost'] * daily_holding_rate


def run_simulation(data, policy_col):
    """
    Runs a real-time inventory simulation and returns detailed daily flow records.
    """
    profit_list = []
    inventory_list = []
    stockout_count = 0
    current_inventory = 0.0

    for idx, row in data.iterrows():
        # 1. Replenishment
        target = np.ceil(row[policy_col])
        order_qty = max(0, target - current_inventory)
        stock_available = current_inventory + order_qty

        # 2. Sales
        demand = row['sales']
        sales = min(stock_available, demand)

        if demand > stock_available:
            stockout_count += 1

        # 3. Settlement and Spoilage
        unsold = stock_available - sales
        spoilage = unsold * daily_spoilage_rate
        current_inventory = unsold - spoilage  # Carry over to tomorrow

        # 4. Financial Calculation
        revenue = sales * row['sell_price']
        cogs = sales * row['cost']
        holding = unsold * row['daily_holding_unit_cost']
        spoil_cost = spoilage * row['cost']

        daily_net_profit = revenue - cogs - holding - spoil_cost

        profit_list.append(daily_net_profit)
        inventory_list.append(stock_available) # Record peak inventory level of the day

    return profit_list, inventory_list, stockout_count

# Run both strategies
profits_q50, inv_q50, out_q50 = run_simulation(df, policy_col='pred_q50')
profits_q90, inv_q90, out_q90 = run_simulation(df, policy_col='pred_q90')

# Write results back to DataFrame for plotting
