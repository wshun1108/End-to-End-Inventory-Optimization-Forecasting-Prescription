@st.cache_data
def load_data():
    # Load the prediction results generated by train_model.py
    # Ensure this path is correct relative to where you run the 'streamlit run' command.
    # Based on your screenshot, running from root, this path is correct:

    current_dir = os.path.dirname(os.path.abspath(__file__))
    processed_data_path = os.path.join(os.path.dirname(current_dir), 'data', 'processed')
    path = os.path.join(processed_data_path, 'm5_predictions.parquet')

